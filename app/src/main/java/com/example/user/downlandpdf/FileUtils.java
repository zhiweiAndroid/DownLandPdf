    /**
     *  执行拷贝Assets任务
     *  @param file file
     *  @return 拷贝成功后的目标文件句柄
     *  @throws java.io.IOException
     */
    public static File assetsCopy(Context context, String file, String toPath )  {
        try {
            InputStream source =context.getResources().getAssets().open(file);
            File destinationFile = new File(toPath, file);
            destinationFile.getParentFile().mkdirs();
            OutputStream destination = new FileOutputStream(destinationFile);
            byte[] buffer = new byte[1024];
            int nread;
            while ((nread = source.read(buffer)) != -1) {
                if (nread == 0) {
                    nread = source.read();
                    if (nread < 0)
                        break;
                    destination.write(nread);
                    continue;
                }
                destination.write(buffer, 0, nread);
            }
            destination.close();
            source.close();
            return destinationFile;
        }catch (Exception e){
            return null;
        }

    }

